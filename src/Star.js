import { useState } from "react";
import PropTypes from "prop-types";

const StarComponentStyle = {
  display: "flex",
  alignItems: "center",
  gap: "5px",
};

const StarStyle = {
  height: "48px",
  width: "48px",
  display: "flex",
};

const textStyle = {
  lineHeight: "1",
  margin: "0",
  color: "#0067A5",
  fontSize: "20px",
};

StarComponent.prototype = {
  maxRating: PropTypes.number,
  rating: PropTypes.number,
  onSetRating: PropTypes.func,
};

export default function StarComponent({ maxRating = 5, rating, onSetRating }) {
  const [tempRate, setTempRate] = useState(0);

  function handleRating(rating) {
    onSetRating(rating);
  }
  return (
    <div style={StarComponentStyle}>
      {Array.from({ length: maxRating }, (_, index) => {
        const rate = tempRate ? tempRate : rating;
        const fullBool = index + 1 <= rate;
        return (
          <Star
            key={index}
            onRating={handleRating}
            index={index}
            isFull={fullBool}
            onStarHover={setTempRate}
          />
        );
      })}
      <p style={textStyle}>{tempRate ? tempRate : rating}</p>
    </div>
  );
}

function Star({ onRating, index, isFull, onStarHover }) {
  return (
    <span
      role="button"
      style={StarStyle}
      onClick={() => onRating(index + 1)}
      onMouseEnter={() => onStarHover(index + 1)}
      onMouseLeave={() => onStarHover(0)}
    >
      {isFull ? (
        <svg
          xmlns="http://www.w3.org/2000/svg"
          viewBox="0 0 20 20"
          fill="#4166f5"
          stroke="#00FFEF"
        >
          <path d="M9.049 2.927c.3-.921 1.603-.921 1.902 0l1.07 3.292a1 1 0 00.95.69h3.462c.969 0 1.371 1.24.588 1.81l-2.8 2.034a1 1 0 00-.364 1.118l1.07 3.292c.3.921-.755 1.688-1.54 1.118l-2.8-2.034a1 1 0 00-1.175 0l-2.8 2.034c-.784.57-1.838-.197-1.539-1.118l1.07-3.292a1 1 0 00-.364-1.118L2.98 8.72c-.783-.57-.38-1.81.588-1.81h3.461a1 1 0 00.951-.69l1.07-3.292z" />
        </svg>
      ) : (
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          stroke="#00FFEF"
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth="{2}"
            d="M11.049 2.927c.3-.921 1.603-.921 1.902 0l1.519 4.674a1 1 0 00.95.69h4.915c.969 0 1.371 1.24.588 1.81l-3.976 2.888a1 1 0 00-.363 1.118l1.518 4.674c.3.922-.755 1.688-1.538 1.118l-3.976-2.888a1 1 0 00-1.176 0l-3.976 2.888c-.783.57-1.838-.197-1.538-1.118l1.518-4.674a1 1 0 00-.363-1.118l-3.976-2.888c-.784-.57-.38-1.81.588-1.81h4.914a1 1 0 00.951-.69l1.519-4.674z"
          />
        </svg>
      )}
    </span>
  );
}
